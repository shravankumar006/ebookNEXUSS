import React, { useEffect, useState } from "react";
import Cards from "./Cards";
import axios from "axios";
import { Link } from "react-router-dom";
function Course() {
  const [book, setBook] = useState([]);
  useEffect(() => {
    const getBook = async () => {
      try {
        const res = await axios.get("http://localhost:4000/book");
        console.log(res.data);
        setBook(res.data);
      } catch (error) {
        console.log(error);
      }
    };
    getBook();
  }, []);
  return (
    <>
      <div className=" max-w-screen-2xl container mx-auto md:px-20 px-4">
        <div className="mt-28 items-center justify-center text-center">
          <h1 className="text-2xl  md:text-4xl">
            We're delighted to have you{" "}
            <span className="text-pink-500"> Here! :)</span>
          </h1>
          <p className="mt-12">
          Introducing "BookSphere," a cutting-edge online bookstore designed to revolutionize the way book lovers discover, purchase, 
          and enjoy their favorite titles. Our platform offers an extensive collection of books across all genres, catering to diverse 
          reading preferences. BookSphere combines a user-friendly interface with personalized recommendations, ensuring that every reader 
          finds their perfect match. Our secure payment system guarantees a safe shopping experience, while our seamless delivery service 
          ensures that books reach customers swiftly and in pristine condition. With exclusive access to new releases, bestsellers, and rare finds,
          BookSphere is not just a bookstore but a literary haven. Join us on this literary journey and indulge in the joy of reading like never before.
          </p>
          <Link to="/">
            <button className="mt-6 bg-pink-500 text-white px-4 py-2 rounded-md hover:bg-pink-700 duration-300">
              Back
            </button>
          </Link>
        </div>
        <div className="mt-12 grid grid-cols-1 md:grid-cols-4">
          {book.map((item) => (
            <Cards key={item.id} item={item} />
          ))}
        </div>
      </div>
    </>
  );
}

export default Course;
